<strong>Renews in/Expires Explanation</strong>
<p>This function allows you to set the auto-renewal function such that the training will be automatically 
    reassigned to be due for a person after 30 days has passed.</p>
<p>Example: If you have a training that renews every year (365 days), and you have a user who completed 
    the training 335 days ago, the system will automatically assign that user the same training to be 
    done again. And will have the due date set for the 365th day.</p>
<p>If you set a value of 0, then the training will NOT auto-renew.</p>

<strong>Assignment Options</strong>
<p>When creating a training, you may perform one of 2 tasks.</p>
<ul class="browser-default">
    <li>None - Assign no one to this training at this time.</li>
    <li>Due Date - Assign one or more people to complete this training on a specific date. If you select 
        a due date assignment, you MUST set a due date.</li>
</ul>

<strong>Administrative</strong>
<p>When checked, training displays in the administrative section of the user's profile. As well, email notifications are not sent to assigned users.</p>